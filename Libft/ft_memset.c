#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		*(char *)(b) = (unsigned char)c;
		b++;
		i++;
	}
	return (b - len);
}
