#include "push_swap.h"

size_t	ft_strlen(const char *str)
{
	size_t length;

	length = 0;
	while (str[length])
		length++;
	return (length);
}

void	ft_putstr(char *s)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
	}
}

int	ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}
